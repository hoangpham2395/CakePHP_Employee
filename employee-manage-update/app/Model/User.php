	public function validate_login() {
		$this->validate = array(
			'username' => array(
				'rule1' => array(
					'rule' => array('notBlank'),
					'message' => 'A username is required.',
					'allowEmpty' => false
				),
				'rule2' => array(
					'rule' => array('lengthBetween', 3, 10),
					'required' => true,
					'message' => 'Username must be between 3 to 10 characters.'
				), 
				'rule3' => array(
					'rule' => '/^[a-zA-Z0-9_]{3,10}$/i',
					'message' => 'Username is not correct.'
				)
			),
			'password' => array(
				'rule1' => array(
					'rule' => array('notBlank'),
					'message' => 'A password is required.'
				),
				'rule2' => array(
					'rule' => array('minLength', 6),
					'message' => 'Password must have a mimimum of 6 characters.'
				)
			)
		);
		return $this->validates($this->validate);
	}

/**
 * Before Save
 * @param array $options
 * @return boolean
